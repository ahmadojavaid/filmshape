
import 'package:Filmshape/Model/SignUp/SignupResponse.dart';

class CreateProfileResponse {
  String type;
  int id;
  List<Roles> roles;
  num height;
  num weight;
  Gender gender;
  Ethnicity ethnicity;
  String bio;
  String media;
  String mediaEmbed;
  String created;
  UserData user;

  CreateProfileResponse({this.type,
    this.id,
    this.roles,
    this.height,
    this.weight,
    this.gender,
    this.ethnicity,
    this.bio});

  CreateProfileResponse.fromJson(Map<String, dynamic> json) {
    type = json['type'];
    id = json['id'];
    if (json['roles'] != null) {
      roles = new List<Roles>();
      json['roles'].forEach((v) {
        roles.add(new Roles.fromJson(v));
      });
    }
    height = json['height'];
    weight = json['weight'];
    gender =
    json['gender'] != null ? new Gender.fromJson(json['gender']) : null;
    ethnicity = json['ethnicity'] != null
        ? new Ethnicity.fromJson(json['ethnicity'])
        : null;
    bio = json['bio'];
    media=json['media'];
    mediaEmbed=json['media_embed'];
    created=json['created'];

    user = json['user'] != null ? new UserData.fromJson(json['user']) : null;

  }

  Map<String, dynamic> toJson() {
    final Map<String, dynamic> data = new Map<String, dynamic>();
    data['type'] = this.type;
    data['id'] = this.id;
    if (this.roles != null) {
      data['roles'] = this.roles.map((v) => v.toJson()).toList();
    }
    data['height'] = this.height;
    data['weight'] = this.weight;
    if (this.gender != null) {
      data['gender'] = this.gender.toJson();
    }
    if (this.ethnicity != null) {
      data['ethnicity'] = this.ethnicity.toJson();
    }
    data['bio'] = this.bio;
    data['media'] = this.media;
    data['media_embed'] = this.mediaEmbed;

    return data;
  }
}
